const flashcardName = document.getElementById('flashcard-name');
const flashcardHint = document.getElementById('flashcard-hint');
const flashcardHintBox = document.getElementById('flashcard-hint-box');
const flashcardPrev = document.getElementById('flashcard-prev');
const flashcardNext = document.getElementById('flashcard-next');
const flashcardShuffle = document.getElementById('flashcard-shuffle');

let flashcardList = [];
let flashcardIndex = 0;
let flashcardMode = 'plant'; // 'plant' 或 'family'

btnFlashcardPlant.addEventListener('click', () => {
    startFlashcards('plant', true);
});

btnFlashcardFamily.addEventListener('click', () => {
    startFlashcards('family', true);
});

function startFlashcards(mode = 'plant', shuffle = true) {
    flashcardMode = mode;
    flashcardTitle.textContent = mode === 'plant' ? '植物單字卡' : '科別單字卡';
    
    // 複製對應題庫
    flashcardList = mode === 'plant' ? [...plantDictionary] : [...familyData];
    if (shuffle) {
        flashcardList.sort(() => 0.5 - Math.random());
    }
    flashcardIndex = 0;

    // 確保卡片未翻轉
    flashcardContainer.classList.remove('flipped');

    showScreen(flashcardScreen);
    loadFlashcard();
}

function loadFlashcard() {
    if (flashcardList.length === 0) return;

    const activeCard = flashcardList[flashcardIndex];
    flashcardProgress.textContent = `第 ${flashcardIndex + 1} / ${flashcardList.length} 張`;

    // 每次切換都強制翻回正面
    flashcardContainer.classList.remove('flipped');

    // 處理正面
    if (flashcardMode === 'plant') {
        flashcardFrontText.style.display = 'none';
        flashcardImage.style.display = 'none';
        flashcardPlaceholder.style.display = 'flex';

        const imgUrl = `images/${activeCard.answer}.png`;
        flashcardImage.src = imgUrl;
        flashcardImage.onload = () => {
            flashcardImage.style.display = 'block';
            flashcardPlaceholder.style.display = 'none';
        };
        flashcardImage.onerror = () => {
            flashcardImage.style.display = 'none';
            flashcardPlaceholder.style.display = 'flex';
        };
    } else {
        // 科別單字卡正面顯示中文科名
        flashcardImage.style.display = 'none';
        flashcardPlaceholder.style.display = 'none';
        flashcardFrontText.style.display = 'block';
        flashcardFrontText.textContent = activeCard.chinese;
    }

    // 處理背面文字
    if (flashcardMode === 'plant') {
        flashcardBackText.style.display = 'none';
        flashcardName.style.display = 'block';
        flashcardName.textContent = activeCard.answer;
        
        flashcardBack.style.alignItems = 'flex-start';
        flashcardBack.style.justifyContent = 'flex-start';
        flashcardBack.style.textAlign = 'left';

        if (activeCard.hint && activeCard.hint.trim() !== '') {
            flashcardHint.innerHTML = activeCard.hint.replace(/\n/g, '<br>');
            flashcardHintBox.style.display = 'block';
        } else {
            flashcardHintBox.style.display = 'none';
        }
    } else {
        // 科別單字卡背面顯示拉丁科名 (置中白字)
        flashcardName.style.display = 'none';
        flashcardHintBox.style.display = 'none';
        
        flashcardBackText.style.display = 'block';
        flashcardBackText.textContent = activeCard.latin;
        
        // 置中樣式
        flashcardBack.style.alignItems = 'center';
        flashcardBack.style.justifyContent = 'center';
        flashcardBack.style.textAlign = 'center';
    }

    // 更新按鈕狀態
    flashcardPrev.disabled = (flashcardIndex === 0);
    flashcardNext.disabled = (flashcardIndex === flashcardList.length - 1);
}

flashcardContainer.addEventListener('click', () => {
    flashcardContainer.classList.toggle('flipped');
});

flashcardPrev.addEventListener('click', (e) => {
    e.stopPropagation(); // 避免觸發翻轉
    if (flashcardIndex > 0) {
        flashcardIndex--;
        loadFlashcard();
    }
});

flashcardNext.addEventListener('click', (e) => {
    e.stopPropagation();
    if (flashcardIndex < flashcardList.length - 1) {
        flashcardIndex++;
        loadFlashcard();
    }
});

flashcardShuffle.addEventListener('click', (e) => {
    e.stopPropagation();
    startFlashcards(flashcardMode, true);
});

/* =========================================
   模式 2: 植物辨認幻燈片 (PPT)
========================================= */
const currentQuestionNum = document.getElementById('current-question-num');
const timerBar = document.getElementById('timer-bar');
const timerText = document.getElementById('timer-text');
const plantImage = document.getElementById('plant-image');
const imagePlaceholder = document.getElementById('image-placeholder');
const optionsContainer = document.getElementById('options-container');

let plantCurrentIndex = 0;
let plantAnswers = [];
let selectedPlantCount = 40;
let currentIdentificationQuestions = [];
const TIME_LIMIT = 30; // 30秒
let timeLeft = TIME_LIMIT;
let timerInterval = null;

btnIdentification.addEventListener('click', () => {
    startIdentification();
});

function startIdentification() {
    plantCurrentIndex = 0;
    plantAnswers = [];

    // 獲取選擇的測驗題數
    const selectEl = document.getElementById('plant-num-select');
    selectedPlantCount = parseInt(selectEl.value, 10);

    // 隨機抽取指定數量的植物
    const shuffled = [...plantDictionary].sort(() => 0.5 - Math.random());
    currentIdentificationQuestions = shuffled.slice(0, selectedPlantCount);

    showScreen(identificationScreen);
    loadPlantQuestion();
}

function loadPlantQuestion() {
    if (plantCurrentIndex >= currentIdentificationQuestions.length) {
        finishIdentification();
        return;
    }

    // 初始化畫面與倒數
    timeLeft = TIME_LIMIT;
    currentQuestionNum.textContent = `第 ${plantCurrentIndex + 1} / ${currentIdentificationQuestions.length} 題`;
    updateTimerUI();

    const activePlant = currentIdentificationQuestions[plantCurrentIndex];

    plantImage.style.display = 'none';
    imagePlaceholder.style.display = 'flex';

    // 改為根據中文答案去抓取同名的 png 圖片 (例如：當歸.png)
    const imgUrl = `images/${activePlant.answer}.png`;
    plantImage.src = imgUrl;
    plantImage.onload = () => {
        plantImage.style.display = 'block';
        imagePlaceholder.style.display = 'none';
    };
    plantImage.onerror = () => {
        // 如果載入失敗就顯示 placeholder
        plantImage.style.display = 'none';
        imagePlaceholder.style.display = 'flex';
    };

    // 生成這題的選項 (1個正確, 9個錯誤)
    let options = [activePlant.answer];
    let others = plantDictionary.map(p => p.answer).filter(a => a !== activePlant.answer);
    others.sort(() => 0.5 - Math.random());
    options = options.concat(others.slice(0, 9));
    options.sort(() => 0.5 - Math.random()); // 再次打亂

    optionsContainer.innerHTML = '';
    options.forEach(opt => {
        const btn = document.createElement('button');
        btn.className = 'option-btn';
        btn.textContent = opt;
        btn.onclick = () => handleOptionClick(btn, opt, activePlant);
        optionsContainer.appendChild(btn);
    });

    // 開始計時
    if (timerInterval) clearInterval(timerInterval);
    timerInterval = setInterval(() => {
        timeLeft--;
        updateTimerUI();
        if (timeLeft <= 0) {
            handleTimeout(activePlant);
        }
    }, 1000);
}

function updateTimerUI() {
    timerText.textContent = `${timeLeft}s`;
    let pct = (timeLeft / TIME_LIMIT) * 100;
    timerBar.style.width = `${pct}%`;

    timerBar.className = 'timer-bar'; // reset class
    if (timeLeft <= 10) {
        timerBar.classList.add(timeLeft <= 5 ? 'danger' : 'warning');
    }
}

function handleOptionClick(clickedBtn, selectedAnswer, activePlant) {
    if (timerInterval) clearInterval(timerInterval); // 停止計時

    const allBtns = optionsContainer.querySelectorAll('.option-btn');
    allBtns.forEach(b => b.disabled = true);

    // 僅給予視覺上的選取回饋，不提示對錯
    clickedBtn.classList.add('selected');

    const isCorrect = (selectedAnswer === activePlant.answer);
    plantAnswers.push({
        id: activePlant.id,
        hint: activePlant.hint,
        prompt: `圖片 ${activePlant.id}`,
        userAnswer: selectedAnswer,
        correctAnswer: activePlant.answer,
        isCorrect: isCorrect
    });

    // 稍微延遲讓學生知道自己按了什麼，接著迅速下一題
    setTimeout(() => {
        plantCurrentIndex++;
        loadPlantQuestion();
    }, 400); // 縮短為 0.4 秒，更像真實考試
}

function handleTimeout(activePlant) {
    if (timerInterval) clearInterval(timerInterval);

    const allBtns = optionsContainer.querySelectorAll('.option-btn');
    allBtns.forEach(b => b.disabled = true);

    // 不給予提示對錯，直接計為未作答
    plantAnswers.push({
        id: activePlant.id,
        hint: activePlant.hint,
        prompt: `圖片 ${activePlant.id}`,
        userAnswer: '(未作答)',
        correctAnswer: activePlant.answer,
        isCorrect: false
    });

    // 時間到直接跳轉，不需要延遲太久
    setTimeout(() => {
        plantCurrentIndex++;
        loadPlantQuestion();
    }, 400);
}

function finishIdentification() {
    if (timerInterval) clearInterval(timerInterval);
    let correctCount = plantAnswers.filter(a => a.isCorrect).length;
    showResults(correctCount, currentIdentificationQuestions.length, plantAnswers, 'identification');
}

/* =========================================
   結果結算畫面
========================================= */
const finalScore = document.getElementById('final-score');
const resultsList = document.getElementById('results-list');

function showResults(score, total, resultsData, type) {
    const accuracy = Math.round((score / total) * 100) || 0;
    finalScore.innerHTML = `${score} / ${total} <div style="font-size: 1.5rem; color:#94a3b8; margin-top: 0.5rem">正確率: ${accuracy}%</div>`;

    resultsList.innerHTML = '';
    resultsData.forEach(item => {
        const div = document.createElement('div');
        div.className = `result-item ${item.isCorrect === true ? 'correct' : (item.isCorrect === false ? 'incorrect' : '')}`;

        let answerHtml = '';
        if (item.isCorrect === false) {
            answerHtml = `<span class="result-answer wrong">${item.userAnswer}</span> <span class="result-correct-answer">${item.correctAnswer}</span>`;
        } else if (item.isCorrect === true) {
            answerHtml = `<span class="result-answer" style="color: var(--success-color);">${item.userAnswer}</span>`;
        } else {
            // 待批改模式
            answerHtml = `<span class="result-answer">${item.userAnswer}</span>`;
        }

        let iconHtml = '';
        if (item.isCorrect === true) iconHtml = '<span class="status-icon">✅</span>';
        else if (item.isCorrect === false) iconHtml = '<span class="status-icon">❌</span>';
        else iconHtml = '<span class="status-icon">📝</span>';

        let promptHtml = `<div class="result-prompt">${item.prompt}</div>`;
        if (type === 'identification') {
            promptHtml = `
                <div class="result-thumbnail">
                    <img src="images/${item.correctAnswer}.png" alt="縮圖" onerror="this.parentElement.style.display='none'">
                </div>
            `;
        }

        div.innerHTML = `
            ${promptHtml}
            <div style="flex:1">${answerHtml}</div>
            ${iconHtml}
        `;

        if (type === 'identification') {
            div.classList.add('clickable');
            div.title = "點擊查看詳解";
            div.addEventListener('click', () => {
                showReviewModal(item);
            });
        }

        resultsList.appendChild(div);
    });

    showScreen(resultsScreen);
}

